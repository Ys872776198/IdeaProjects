-- where字句
-- 	where + 关系运算符
select * from EMP WHERE JOB = "CLERK";		-- 默认是不区分大小写的
select * from EMP WHERE BINARY JOB = "CLERK";	-- BINARY是用来区分大小写的
select * from EMP WHERE HIREDATE < "1981-11-17";

-- 	where + 逻辑运算符
-- 		与
select * from EMP WHERE SAL > 1500 && SAL < 3000 ORDER BY SAL ASC;	-- (1500,3000)不包含两极
select * from EMP WHERE SAL > 1500 AND SAL < 3000 ORDER BY SAL ASC;	-- (1500,3000)不包含两极
select * from EMP WHERE SAL BETWEEN 1500 AND 3000;									-- [1500,3000]包含两极

-- 		或
select * from EMP WHERE SAL > 1500 || SAL < 3000 ORDER BY SAL ASC;	-- (1500,3000)不包含两极
select * from EMP WHERE SAL > 1500 OR SAL < 3000 ORDER BY SAL ASC;	-- (1500,3000)不包含两极
select * from EMP WHERE SAL IN (1500,3000);													-- 代表SAL的值选择1500或者3000
select * from EMP WHERE JOB IN ("ANALYST", "SALESMAN", "CLERK") ORDER BY SAL ASC ;

-- 	模糊查询
-- 		% 代表多个字符
select * from EMP WHERE ENAME LIKE "%A";		-- 这代表最后一个字符为A
select * from EMP WHERE ENAME LIKE "%A%";		-- 这代表查找ENAME里面带有A的数据

-- 		- 代表一个字符
select * from EMP WHERE ENAME LIKE "_A%";		-- 这代表第二个字符为A
select * from EMP WHERE ENAME LIKE "%A_";		-- 这代表倒数第二个字符为A

-- 	判读是否非空
select * from EMP WHERE COMM IS NULL;
select * from EMP WHERE COMM IS NOT NULL;



